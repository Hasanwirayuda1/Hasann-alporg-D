#nilai dalama suhu
celcius = 33
celcius = int(input('masukan suhu dalam celcius:'))
print("nilai suhu adalah",celcius)
#reamur
reamur = (4/5) * celcius
print("nilai suhu adalah",reamur,"reamur")
#fahrenheit
fahrenheit = ((9/5) * celcius) + 32
print("nilai suhu adalah",fahrenheit,"fahrenheit")
#kelvin
kelvin = celcius + 273
print("nilai suhu adalah",kelvin,"kelvin")